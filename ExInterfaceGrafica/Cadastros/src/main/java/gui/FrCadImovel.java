    public void objetoParaCampos(Imovel imovel){
        edtTipo.setText(imovel.getTipoImovel());
        edtQuartos.setText(imovel.getQtdQuartos()+ "");
        edtBanheiros.setText(imovel.getQtdBanheiros() + "");
        edtArea.setText(imovel.getTamArea() + "");
        edtPreco.setText(imovel.getPreco() + "");
    }
    public Imovel camposParaObjeto(){
        Imovel imovel = new Imovel();
        imovel.setTipoImovel(edtTipo.getText());
        
        String quartosStr = edtQuartos.getText();
        if(!quartosStr.isEmpty()){
            int quartos = Integer.parseInt(quartosStr);
            imovel.setQtdQuartos(quartos);
        }
        String banheirosStr = edtBanheiros.getText();
        if(!banheirosStr.isEmpty()){
            int banheiros = Integer.parseInt(banheirosStr);
            imovel.setQtdBanheiros(banheiros);
        }
        String areaStr = edtArea.getText();
        if(!areaStr.isEmpty()){
            double area = Double.parseDouble(areaStr);
            imovel.setTamArea(area);
        }
        
        String precoStr = edtPreco.getText();
        if(!precoStr.isEmpty()){
            double preco = Double.parseDouble(precoStr);
            imovel.setPreco(preco);
        }
        return imovel;
    }
