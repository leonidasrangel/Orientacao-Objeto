    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        this.imovelEscolhido = JOptionPane.showInputDialog("Informe o tipo do imóvel: ", "");
        Imovel imovelEditado = this.gerente.buscarImovel(imovelEscolhido);

        if(imovelEditado != null){
            this.editando = true;
            this.limparCampos();
            this.habilitarCampos(true);

            objetoParaCampos(imovelEditado);

        }else{
            System.out.println("Nome do imóvel inválido ou sem cadastro.");
            JOptionPane.showMessageDialog(this , "Nome do imóvel inválido ou sem cadastro.");
        }
    }//GEN-LAST:event_btnEditarActionPerformed

