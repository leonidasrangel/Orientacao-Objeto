    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        this.imovelEscolhido = JOptionPane.showInputDialog("Informe o tipo do imóvel: ", "");

        Imovel imovel = this.gerente.buscarImovel(imovelEscolhido);

        if(imovel == null){
            JOptionPane.showMessageDialog(this, "Nome do imóvel inválido ou sem cadastro.");
        }else{
            this.gerente.removerImovel(imovelEscolhido);
            JOptionPane.showMessageDialog(this, "Nome do imóvel excluído com sucesso");
        }
        String listagem = this.gerente.toString();
        edtListagem.setText(listagem);
    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        Imovel novoImovel = this.camposParaObjeto();

        if(this.editando == true)
        this.gerente.atualizarImovel(this.imovelEscolhido , novoImovel);
        else
        this.gerente.adicionarImovel(novoImovel);

        this.limparCampos();
        this.habilitarCampos(false);
        this.editando = false;

        String listagem = gerente.toString();
        edtListagem.setText(listagem);

        gerente.salvarEmXML("ListagemImoveis.xml"); // Isso salva em arquivo xml
        //gerente.salvarNoArquivo("ListagemImoveis.json"); //Isso salva em arquivo .json
        //gerente.salvarEmCSV("ListagemImoveis.csv"); // Isso salva em arquivo CSV
    }//GEN-LAST:event_btnSalvarActionPerformed

